package com.study.Stage1.Section3.Task1;

public class MathTest {

    public static void main(String[] args) {

        System.out.println("两个数中最大值" + Math.max(10, 20));
        System.out.println("两个数中最小值" + Math.min(10, 20));
        System.out.println("幂运算" + Math.pow(2, 3));
        System.out.println("绝对值" + Math.abs(-3));
        System.out.println("四舍五入" + Math.round(3.14));
        System.out.println("平方根" + Math.sqrt(9));
        System.out.println("随机数" + Math.random());
    }
}
